    private void loadcombo() {
        DefaultListModel lm = new DefaultListModel();
        lm.clear();
        Vector<String> v = new Vector<>();
        v.add("select time duration");
        for (Map.Entry<String, String> entry : addpinv.periodmap.entrySet()) {
            v.add(entry.getKey());
            lm.addElement(entry.getKey()+" -> "+entry.getValue());
        }
        DefaultComboBoxModel model = new DefaultComboBoxModel(v);
        jComboBox1.setModel(model);
        jList1.setModel(lm);
        
    }

