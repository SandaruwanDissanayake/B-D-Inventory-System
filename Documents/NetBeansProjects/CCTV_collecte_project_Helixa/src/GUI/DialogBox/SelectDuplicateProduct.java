    private void loadTable() {

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        model.setRowCount(0);
        int indexSelectProductNumber = 0;
        try {
            ResultSet resultSet = MySQL.execute("SELECT * FROM `product` WHERE `model_number`='" + this.productModel + "'");

            while (resultSet.next()) {
                indexSelectProductNumber++;

                Vector<Object> vector = new Vector<>();

                vector.add(indexSelectProductNumber);
                vector.add(resultSet.getString("model_number"));
                vector.add(resultSet.getString("title"));

                model.addRow(vector);
            }
            jTable1.setModel(model);

        } catch (Exception e) {
                        Login.log1.warning(e.getMessage());

        }

    }

