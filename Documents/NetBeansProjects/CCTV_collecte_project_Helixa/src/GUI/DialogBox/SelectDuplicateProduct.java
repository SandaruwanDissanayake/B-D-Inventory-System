    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        int selectedRow = jTable1.getSelectedRow();

        String modelNumber = String.valueOf(jTable1.getValueAt(selectedRow, 1));
        String title = String.valueOf(jTable1.getValueAt(selectedRow, 2));

        try {
            ResultSet resultSet = MySQL.execute("SELECT * FROM `product` WHERE `model_number`='" + modelNumber + "' AND `title`='" + title + "'");

            if (resultSet.next()) {
                this.selectProductBean.setModelNumber(modelNumber);
                this.selectProductBean.setTitle(title);
                this.selectProductBean.setBrandNumber(resultSet.getInt("brand_id"));
                this.selectProductBean.setCategoryNumber(resultSet.getInt("category_id"));
                this.selectProductBean.setDescription(resultSet.getString("description"));
                this.addproducttogrnPanel.selectDuplicateProductData();
            }
            this.dispose();

        } catch (Exception ex) {
                        Login.log1.warning(ex.getMessage());

        }

    }//GEN-LAST:event_jButton1ActionPerformed

