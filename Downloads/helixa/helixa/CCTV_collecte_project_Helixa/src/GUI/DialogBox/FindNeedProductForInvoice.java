    private void loadTable(String model){
        DefaultTableModel m = (DefaultTableModel)jTable1.getModel();
        m.setRowCount(0);
        try {
            ResultSet rs = MySQL.execute("SELECT * FROM `product` WHERE `model_number` = '"+model+"'");
            while (rs.next()) {                
                Vector<String> v = new Vector<>();
                v.add(rs.getString("id"));
                v.add(rs.getString("title"));
                m.addRow(v);
            }
        } catch (Exception e) {
                        Login.log1.warning(e.getMessage());

        }
    }
    
